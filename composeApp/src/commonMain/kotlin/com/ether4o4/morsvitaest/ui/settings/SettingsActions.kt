            onClearPendingNotifications = {},
            onToggleFreeFallback = {},
            onChangeUiScale = {},
            onAddMcpServer = { _, _, _ -> },
            onRemoveMcpServer = {},
            onToggleMcpServer = { _, _ -> },
            onRefreshMcpServer = {},
            onShowAddMcpServerDialog = {},
            onAddPopularMcpServer = {},
            onDownloadLocalModel = {},
            onCancelLocalModelDownload = {},
            onDeleteLocalModel = {},
